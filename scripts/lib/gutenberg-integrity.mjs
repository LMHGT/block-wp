const GUTENBERG_CONSOLE_SIGNATURES = [
  ['block-validation-failed', /block validation(?:\s*:|\s+failed)/i],
  ['save-content-mismatch', /content generated by the [`'“”]?save[`'“”]? function/i],
  ['post-body-content-mismatch', /content retrieved from post body/i],
  ['unexpected-invalid-content', /unexpected or invalid content/i],
  ['block-recovery', /attempt block recovery/i],
];

export function classifyGutenbergConsoleError(message) {
  const text = String(message || '');
  return GUTENBERG_CONSOLE_SIGNATURES
    .filter(([, pattern]) => pattern.test(text))
    .map(([classification]) => classification);
}

export function classifyExpectedConsoleError(message) {
  return /^Failed to load resource: net::ERR_BLOCKED_BY_CLIENT(?:\.Inspector)?$/i.test(
    String(message || '').trim(),
  )
    ? 'expected-read-only-barrier-resource-block'
    : null;
}

export function classifySameOriginReadFailure({
  duringNavigation,
  errorText,
  method,
  target,
}) {
  const normalizedMethod = String(method || '').toUpperCase();
  const normalizedError = String(errorText || '');
  const normalizedTarget = String(target || '');
  if (
    normalizedMethod === 'OPTIONS'
    && normalizedTarget === '/wp-json/wp/v2/settings'
    && normalizedError === 'net::ERR_ABORTED'
  ) {
    return 'expected-editor-settings-preflight-abort';
  }
  if (
    duringNavigation === true
    && ['GET', 'HEAD', 'OPTIONS'].includes(normalizedMethod)
    && normalizedError === 'net::ERR_ABORTED'
  ) {
    return 'expected-navigation-read-abort';
  }
  return null;
}

export function strictRestCountValue(rawValue, headerName) {
  if (typeof rawValue !== 'string' || !/^\d+$/.test(rawValue)) {
    throw new Error(`WordPress REST response has a missing or malformed ${headerName} header.`);
  }
  const value = Number.parseInt(rawValue, 10);
  if (!Number.isSafeInteger(value) || value < 0) {
    throw new Error(`WordPress REST response has an out-of-range ${headerName} header.`);
  }
  return value;
}

export function comparePublishedInventories(restItems, wpCliItems) {
  if (!Array.isArray(restItems) || !Array.isArray(wpCliItems)) {
    throw new TypeError('Inventory comparison requires two arrays.');
  }
  const identity = (item, typeKey) => JSON.stringify([
    item[typeKey],
    Number(item.id),
    item.status,
    item.slug,
  ]);
  const restIdentities = restItems.map((item) => identity(item, 'expectedPostType'));
  const wpCliIdentities = wpCliItems.map((item) => identity(item, 'type'));
  const restSet = new Set(restIdentities);
  const wpCliSet = new Set(wpCliIdentities);
  const missingFromRest = [...wpCliSet].filter((item) => !restSet.has(item));
  const missingFromWpCli = [...restSet].filter((item) => !wpCliSet.has(item));
  const duplicateRestRecords = restIdentities.filter(
    (item, index) => restIdentities.indexOf(item) !== index,
  );
  const duplicateWpCliRecords = wpCliIdentities.filter(
    (item, index) => wpCliIdentities.indexOf(item) !== index,
  );
  return {
    duplicateRestRecords: [...new Set(duplicateRestRecords)],
    duplicateWpCliRecords: [...new Set(duplicateWpCliRecords)],
    missingFromRest,
    missingFromWpCli,
    ok: restItems.length === wpCliItems.length
      && restSet.size === restItems.length
      && wpCliSet.size === wpCliItems.length
      && missingFromRest.length === 0
      && missingFromWpCli.length === 0,
  };
}

export function finalInvariantFailedChecks(summary, contentTypeKeys = ['page', 'post']) {
  const existingFailureCount = summary.failures.filter(
    ({ stage }) => stage !== 'final-invariant',
  ).length;
  const declaredTypeTotal = contentTypeKeys.reduce(
    (total, key) => total + (Number(summary.inventory[key]) || 0),
    0,
  );
  const checks = [
    ['runtime-identity', summary.runtimeIdentity.ok === true],
    ['inventory-nonempty', summary.inventory.total > 0],
    [
      'inventory-parity',
      summary.inventory.parity?.ok === true
        && declaredTypeTotal === summary.inventory.total
        && summary.inventory.restTotal === summary.inventory.total
        && summary.inventory.wpCliTotal === summary.inventory.total,
    ],
    [
      'editor-inventory-coverage',
      summary.counts.scanned === summary.inventory.total
        && summary.counts.passed === summary.inventory.total
        && summary.counts.failed === 0,
    ],
    [
      'source-inventory-coverage',
      summary.sourcePreflight.attempted === true
        && summary.sourcePreflight.expected > 0
        && summary.sourcePreflight.scanned === summary.sourcePreflight.expected,
    ],
    [
      'source-validity',
      summary.sourcePreflight.failed === 0
        && summary.sourcePreflight.passed === summary.sourcePreflight.expected
        && summary.sourcePreflight.passed + summary.sourcePreflight.failed
          === summary.sourcePreflight.scanned,
    ],
    [
      'edit-lock-restoration',
      summary.editLocks.snapshotAttempted === true
        && summary.editLocks.restorationAttempted === true
        && summary.editLocks.snapshotCount === summary.inventory.total
        && summary.editLocks.verifiedCount === summary.editLocks.snapshotCount
        && summary.editLocks.ok === true,
    ],
    [
      'administrator-cleanup',
      summary.cleanup.attempted === true
        && summary.cleanup.creationPreflightAbsent === true
        && summary.cleanup.creationVerified === true
        && summary.cleanup.ok === true,
    ],
    [
      'recovery-marker-cleanup',
      Array.isArray(summary.cleanup.recoveryMarkers)
        && summary.cleanup.recoveryMarkers.length === 0
        && summary.editLocks.recoveryMarker === null,
    ],
    ['read-only-write-barrier', summary.readOnlyGuard.fatalBlocked.count === 0],
    ['recorded-failures', existingFailureCount === 0],
  ];
  return checks.filter(([, ok]) => !ok).map(([name]) => name);
}
