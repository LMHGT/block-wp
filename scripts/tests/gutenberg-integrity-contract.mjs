#!/usr/bin/env node

import assert from 'node:assert/strict';
import {
  classifyExpectedConsoleError,
  classifyGutenbergConsoleError,
  classifySameOriginReadFailure,
  comparePublishedInventories,
  finalInvariantFailedChecks,
  strictRestCountValue,
} from '../lib/gutenberg-integrity.mjs';

assert.deepEqual(
  classifyGutenbergConsoleError('Block validation: Block validation failed for core/group'),
  ['block-validation-failed'],
);
assert.deepEqual(
  classifyGutenbergConsoleError('Content generated by the `save` function differs from content retrieved from post body.'),
  ['save-content-mismatch', 'post-body-content-mismatch'],
);
assert.deepEqual(classifyGutenbergConsoleError('A harmless browser message'), []);
assert.equal(
  classifyExpectedConsoleError('Failed to load resource: net::ERR_BLOCKED_BY_CLIENT.Inspector'),
  'expected-read-only-barrier-resource-block',
);
assert.equal(classifyExpectedConsoleError('TypeError: unexpected application failure'), null);
assert.equal(
  classifySameOriginReadFailure({
    duringNavigation: false,
    errorText: 'net::ERR_ABORTED',
    method: 'OPTIONS',
    target: '/wp-json/wp/v2/settings',
  }),
  'expected-editor-settings-preflight-abort',
);
assert.equal(
  classifySameOriginReadFailure({
    duringNavigation: true,
    errorText: 'net::ERR_ABORTED',
    method: 'GET',
    target: '/wp-content/example.svg',
  }),
  'expected-navigation-read-abort',
);
assert.equal(
  classifySameOriginReadFailure({
    duringNavigation: false,
    errorText: 'net::ERR_FAILED',
    method: 'GET',
    target: '/wp-json/wp/v2/settings',
  }),
  null,
);

assert.equal(strictRestCountValue('0', 'x-wp-total'), 0);
assert.equal(strictRestCountValue('123', 'x-wp-total'), 123);
assert.throws(() => strictRestCountValue('', 'x-wp-total'), /missing or malformed/);
assert.throws(() => strictRestCountValue('1.5', 'x-wp-total'), /missing or malformed/);

const rest = [
  { expectedPostType: 'page', id: 6, status: 'publish', slug: 'home' },
  { expectedPostType: 'post', id: 9, status: 'draft', slug: '' },
];
const cli = [
  { type: 'page', id: 6, status: 'publish', slug: 'home' },
  { type: 'post', id: 9, status: 'draft', slug: '' },
];
assert.equal(comparePublishedInventories(rest, cli).ok, true);
assert.equal(comparePublishedInventories([...rest, rest[0]], cli).ok, false);
assert.equal(comparePublishedInventories(rest.slice(0, 1), cli).ok, false);

const passingSummary = {
  failures: [],
  runtimeIdentity: { ok: true },
  inventory: {
    page: 1,
    post: 1,
    total: 2,
    restTotal: 2,
    wpCliTotal: 2,
    parity: { ok: true },
  },
  counts: { scanned: 2, passed: 2, failed: 0 },
  sourcePreflight: { attempted: true, expected: 4, scanned: 4, passed: 4, failed: 0 },
  editLocks: {
    snapshotAttempted: true,
    restorationAttempted: true,
    snapshotCount: 2,
    verifiedCount: 2,
    recoveryMarker: null,
    ok: true,
  },
  cleanup: {
    attempted: true,
    creationPreflightAbsent: true,
    creationVerified: true,
    ok: true,
    recoveryMarkers: [],
  },
  readOnlyGuard: { fatalBlocked: { count: 0 } },
};
assert.deepEqual(finalInvariantFailedChecks(passingSummary), []);

const failedCleanupSummary = structuredClone(passingSummary);
failedCleanupSummary.cleanup.ok = false;
assert.deepEqual(
  finalInvariantFailedChecks(failedCleanupSummary),
  ['administrator-cleanup'],
);

const incompleteSourceSummary = structuredClone(passingSummary);
incompleteSourceSummary.sourcePreflight.passed = 0;
assert.deepEqual(
  finalInvariantFailedChecks(incompleteSourceSummary),
  ['source-validity'],
);

const ambiguousAdminSummary = structuredClone(passingSummary);
ambiguousAdminSummary.cleanup.creationPreflightAbsent = false;
assert.deepEqual(
  finalInvariantFailedChecks(ambiguousAdminSummary),
  ['administrator-cleanup'],
);

const strandedLockMarkerSummary = structuredClone(passingSummary);
strandedLockMarkerSummary.editLocks.recoveryMarker = 'edit-lock-recovery-run-id.json';
assert.deepEqual(
  finalInvariantFailedChecks(strandedLockMarkerSummary),
  ['recovery-marker-cleanup'],
);

console.log('Gutenberg integrity contract tests passed.');
